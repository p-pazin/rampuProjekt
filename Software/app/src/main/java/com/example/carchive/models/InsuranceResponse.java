package com.example.carchive.models;

public class InsuranceResponse{
	private int cost;
	private String name;
	private String description;
	private int id;

	public int getCost(){
		return cost;
	}

	public String getName(){
		return name;
	}

	public String getDescription(){
		return description;
	}

	public int getId(){
		return id;
	}
}
